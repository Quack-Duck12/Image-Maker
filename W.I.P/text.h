static inline void drawChar(FRAME* f, char chr, vectorI pos, COLOR clr, uint16_t scale) {
    if (chr < 0 || chr >= 128) return;
    const FONT* ch = FONT7X7[(unsigned char)chr];
    if (!ch) return;

    for (uint8_t y = 0; y < 7; ++y) {
        for (uint8_t x = 0; x < 7; ++x) {
            if (ch->bitmap[y][x]) {
                // draw scale Ã— scale block
                for (int dy = 0; dy < scale; ++dy) {
                    for (int dx = 0; dx < scale; ++dx) {
                        vectorI pixel = {
                            pos.X + x * scale + dx,
                            pos.Y + y * scale + dy
                        };
                        setColor(f, pixel, clr);
                    }
                }
            }
        }
    }
}

static inline void drawString(FRAME* f, const char* str, vectorI position, COLOR clr, uint16_t scale) {
    uint16_t len = strlen(str);
    uint8_t width = 0;
    int32_t x = position.X;

    for (uint16_t i = 0; i < len; ++i) {

        if(str[i] == '~') width = 7;
        else if(str[i] == '.' || str[i] == '!') width = 4;
        else width = 6;

        vectorI charPos = {
            x,
            position.Y
        };

        drawChar(f, str[i], charPos, clr, scale);

        x += width * scale;    // move X by character width
    }
}




